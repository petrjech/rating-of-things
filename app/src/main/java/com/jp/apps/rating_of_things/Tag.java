package com.jp.apps.rating_of_things;

public class Tag {

    private long id;
    private String name;

    public Tag(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }
}
